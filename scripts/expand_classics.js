function main() {
  console.log('开始读取原始 Classics 数据...');
  
  if (!fs.existsSync(CLASSICS_PATH_PUBLIC)) {
    console.error('未找到 classics.json 文件！');
    return;
  }

  const rawData = fs.readFileSync(CLASSICS_PATH_PUBLIC, 'utf-8');
  const originalClassics = JSON.parse(rawData);

  console.log(`原数据共有古籍条目数: ${originalClassics.length}`);

  // 数据清洗去重逻辑：排除任何非原生古籍（诗经, 论语, 楚辞, 周易），保留原四部
  const originalFourClassics = originalClassics.filter(item => 
    ['诗经', '论语', '楚辞', '周易'].includes(item.source)
  );

  console.log(`清洗保留原生四部古籍数: ${originalFourClassics.length}`);

  // 合并数据
  const mergedClassics = [...originalFourClassics, ...newClassics];

  console.log(`合并后新数据古籍数: ${mergedClassics.length}`);

  // 写入 public 目录下的 classics.json
  fs.writeFileSync(CLASSICS_PATH_PUBLIC, JSON.stringify(mergedClassics, null, 2), 'utf-8');
  console.log(`成功写回公网发布数据文件: ${CLASSICS_PATH_PUBLIC}`);

  // 写入 src/database 目录下的 classics.json
  fs.writeFileSync(CLASSICS_PATH_SRC, JSON.stringify(mergedClassics, null, 2), 'utf-8');
  console.log(`成功备份至本地开发数据文件: ${CLASSICS_PATH_SRC}`);

  console.log('✨ 典籍扩充合并顺利完成！');
}

main();
